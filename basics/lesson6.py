#control flow
'''
in python we do have some control flow machanism like
if elif and else and then we have loops
in loops we have for loop and while loop
'''

age = 17

if(age >= 18):
    print("can get driving licence")
else:
    print("cannot eligible for the driving licence")


for i in range(age):
    print("from for loop ",i)


j = 10;

while(j <= 20):
    print("from while loop ",j)
    j += 1
